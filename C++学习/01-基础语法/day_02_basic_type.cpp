#include <iostream>

int main() {
    // 基本类型
    int a = 10;
    float b = 10.5;
    double c = 10.5;
    char d = 'a';
    bool e = true;
    return 0;
}

void print_basic_type() {
    int a = 10;
    float b = 10.5;
    double c = 10.5;
    char d = 'a';
    bool e = true;
    std::cout << "a = " << a << std::endl;
    std::cout << "b = " << b << std::endl;
    std::cout << "c = " << c << std::endl;
    std::cout << "d = " << d << std::endl;
    std::cout << "e = " << e << std::endl;
}